#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	t_list	*tmp;
	int		count;

	if (!lst)
		return (0);
	tmp = lst;
	count = 1;
	while (tmp->next)
	{
		count++;
		tmp = tmp->next;
	}
	return (count);
}
